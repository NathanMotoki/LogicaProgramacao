package posNeg;

public class PosNeg {
    
    public void PositivoNegativo(double numero) {
        if(numero > 0) {
            System.out.println("Numero Positivo!");
        } else if (numero < 0) {
            System.out.println("Numero Negativo!");
        } else {
            System.out.println("Numero Zero, ou seja, nulo!");
        }
    } 

}
